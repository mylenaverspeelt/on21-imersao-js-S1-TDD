class Calculadora {
     
  static soma(num1, num2){
      return num1 + num2
    }
  
    static subtrai(num1, num2){
      return num1 - num2
    }
    static   multiplica(num1, num2){
      return num1 * num2
    }
    static  divide(num1, num2){
      return num1 / num2
    }
  }

  module.exports = {Calculadora}